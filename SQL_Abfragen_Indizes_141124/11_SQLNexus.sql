/*
	SQLNexus ist ein Diagnosetool => Leistungsproble in SQL-Server-Umgebungen
	zu analysieren und zu beheben
	=> Importiert und verarbeitet Daten, die mithilfe anderer Tools wie SQL Server Profiler
	gesammelt wurden und erstellt dann visuelle Berichte zur Problemursache


	Hauptfunktionen von SQL Nexus:

	1. Datenimport aus verschiedenen Quellen wie z.B SQL Server Profiler

	2. Analyse und Berichterstellung:
	- generiert detaillierte Berichte, die Leistungsprobleme aufzeigen
	z. B Engpässe bei CPU,Speicher oder Festplatten und gibt Einblicke in die SQL Server Abfrageleistung

	3. Visualisierungen:
	- Stellt Daten visuell dar, die Berichte umfassen Tabellen und Diagramme, die potenziellen Problemen
	 der Serverleistung an

	 4. Automatisierte Leistungsanalyse:
	 - SQLNexus => wertet die gesammlten Daten aus => hebt er die problematischsten
	 Abfragen, Sitzungen und Ressourcen hervor

	 5. Kostenlos und Anpassbar: SQL Nexus ist kostenlos und ermöglicht die Anpassung der Berichte
	 und Analysen an spezifische Anforderungen


	 Einsatzgebiete von SQLNexus:
	 - Eignet sich zur Untersuchung von Leistungsproblemen in SQL Server Umgebungen
	 - SQL Server Administratoren verwenden es häufig zur Optimierung von Datenbankabfragen
	 und -operationen durch analysieren von ineffiziente Abfragen, Locking und hohe CPU- und Speicherlast


*/