/*
	LogScout: ist ein Microsoft-Tool, das zur Diagnose und Analyse von
	SQL Server-Problemen entwickelt wurde

	- Hilft dabei Protokolle und Diagnosedaten von SQL Server-instanzen zu sammeln
		=> Gut für Datenbankadministratoren & Support-Teams

	Wichtige Funktionen von LogScout:

	1. Automatisierte Datensammlung
	- Sammelt automatisch Protokolle, Fehlermeldungen ohne das der Administrator überprüfen muss

	2. Leistungsanalyse:
	- Erfasst Leistungsdaten, um Engpässe oder Leistungsprobleme des SQL Servers zu identifizieren

	3. Flexible Protokollerstellung:
	- gezielte Analyse durch bestimmte Konfiguration von Protokollen oder Diagnosedaten

	4. Einfacher Export:
	- gesammelte Daten lassen sich leicht an den Microsoft-Support oder Analyse-Tools weiterleiten

	5. Reduzierte Fehleranalysezeit:
	- Durch die Automatisierung er Datensammlung kann SQL LogScout die Zeit für Problemidentifikation reduzieren


	Anwendungsfälle:
	- SQL Server Instanzen werden langsamer
	- Fehlermeldungen angezeigt werden
	- Verbindungsprobleme

*/